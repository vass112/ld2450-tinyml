"""
export_results_pdf.py  —  Bundles all 6 result charts into a single PDF report.
Usage:  python export_results_pdf.py
Output: results/LD2450_TinyML_Results.pdf
"""
import os
import matplotlib
matplotlib.use('Agg')
import matplotlib.pyplot as plt
import matplotlib.image as mpimg
from matplotlib.backends.backend_pdf import PdfPages

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
RESULTS_DIR = os.path.join(SCRIPT_DIR, 'tinyml_results')
OUT_PDF     = os.path.join(RESULTS_DIR, 'LD2450_TinyML_Results.pdf')

DARK  = '#0a0f0a'
GREEN = '#00ff88'
GREY  = '#8899aa'

CHARTS = [
    ('01_kalman_filter_noise_reduction.png',
     'Chart 1: Kalman Filter — Noise Reduction',
     'Raw noisy LD2450 sensor output vs Kalman-filtered X/Y positions.\nAnnotated with noise standard deviation (σ) per axis.'),

    ('02_threat_score_timeline.png',
     'Chart 2: Real-time Threat Score Timeline',
     'Threat score over a 60s session showing alert state transitions:\nCLEAR → APPROACHING → THREAT → LOITERING (orange diamonds).'),

    ('03_position_trace_2d.png',
     'Chart 3: 2D Target Position Trace',
     'Scatter plot of raw vs Kalman-filtered positions in mm.\nColour-coded by threat score; blue arrows indicate predicted movement direction.'),

    ('04_classification_distribution.png',
     'Chart 4: Motion Classification Distribution',
     'Breakdown of detected movement classes (STATIC, CREEPING, WALKING, RUNNING, VEHICLE)\nfrom on-device TinyML MLP inference.'),

    ('05_system_comparison_radar.png',
     'Chart 5: System Comparison — Radar Chart',
     'Spider chart comparing LD2450+ESP32 against alternatives (PIR, Ultrasonic, Webcam+CV,\nCommercial Radar, IR Array) across 6 capability axes.'),

    ('06_cost_vs_capability.png',
     'Chart 6: Cost vs Capability',
     'Bubble chart comparing system cost (log scale) against composite capability score.\nLD2450+ESP32 highlighted as best cost-capability ratio.'),
]

def cover_page(pdf):
    fig = plt.figure(figsize=(11.7, 8.3), facecolor=DARK)
    fig.text(0.5, 0.72, 'LD2450 + ESP32 TinyML',
             ha='center', va='center', fontsize=28, fontweight='bold',
             color=GREEN, fontfamily='monospace')
    fig.text(0.5, 0.62, 'Radar-Based Human Presence & Motion Classification System',
             ha='center', va='center', fontsize=14, color=GREY, fontfamily='monospace')
    fig.text(0.5, 0.50, 'Results & Comparison Report',
             ha='center', va='center', fontsize=18, color=GREEN, fontfamily='monospace')

    # Separator line
    ax = fig.add_axes([0.1, 0.47, 0.8, 0.002])
    ax.set_facecolor(GREEN); ax.axis('off')

    fig.text(0.5, 0.38, 'Contains 6 publication-quality charts:', ha='center',
             fontsize=11, color=GREY, fontfamily='monospace')

    entries = [
        '01  Kalman Filter Noise Reduction',
        '02  Real-time Threat Score Timeline',
        '03  2D Target Position Trace',
        '04  Motion Classification Distribution',
        '05  System Comparison Radar Chart',
        '06  Cost vs Capability Scatter',
    ]
    for i, e in enumerate(entries):
        fig.text(0.5, 0.33 - i * 0.05, e, ha='center', fontsize=10,
                 color=GREEN, fontfamily='monospace')

    fig.text(0.5, 0.06, 'Generated by export_results_pdf.py  |  LD2450 TinyML Project',
             ha='center', fontsize=8, color=GREY, fontfamily='monospace')
    pdf.savefig(fig, facecolor=DARK)
    plt.close(fig)
    print('  Cover page done.')


def chart_page(pdf, filename, title, caption):
    img_path = os.path.join(RESULTS_DIR, filename)
    if not os.path.exists(img_path):
        print(f'  [SKIP] Not found: {img_path}')
        return

    img = mpimg.imread(img_path)
    img_h, img_w = img.shape[:2]
    is_portrait = img_h > img_w

    page_w, page_h = (8.3, 11.7) if is_portrait else (11.7, 8.3)
    fig = plt.figure(figsize=(page_w, page_h), facecolor=DARK)

    # Title banner
    fig.text(0.5, 0.96, title, ha='center', va='top', fontsize=13,
             fontweight='bold', color=GREEN, fontfamily='monospace')

    # Image axes
    top_margin    = 0.10
    bottom_margin = 0.10
    side_margin   = 0.04
    ax = fig.add_axes([side_margin, bottom_margin, 1 - 2*side_margin, 1 - top_margin - bottom_margin])
    ax.imshow(img)
    ax.axis('off')
    ax.set_facecolor(DARK)

    # Caption
    fig.text(0.5, 0.04, caption, ha='center', va='bottom', fontsize=9,
             color=GREY, fontfamily='monospace', linespacing=1.5)

    pdf.savefig(fig, facecolor=DARK)
    plt.close(fig)
    print(f'  Added: {filename}')


def main():
    os.makedirs(RESULTS_DIR, exist_ok=True)

    missing = [c[0] for c in CHARTS if not os.path.exists(os.path.join(RESULTS_DIR, c[0]))]
    if missing:
        print(f'[WARNING] Missing chart files: {missing}')
        print('  Run:  python plot_tinyml.py --demo   first.')

    print(f'Building PDF → {OUT_PDF}')
    with PdfPages(OUT_PDF) as pdf:
        # PDF metadata
        d = pdf.infodict()
        d['Title']   = 'LD2450 TinyML Results Report'
        d['Author']  = 'LD2450 TinyML Project'
        d['Subject'] = 'Radar-based Human Presence & Motion Classification'
        d['Keywords'] = 'LD2450, ESP32, TinyML, Kalman, radar, classification'

        cover_page(pdf)
        for filename, title, caption in CHARTS:
            chart_page(pdf, filename, title, caption)

    print(f'\nDone!  PDF saved to:\n  {OUT_PDF}')


if __name__ == '__main__':
    main()
